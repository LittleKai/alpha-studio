  function reviewModel(input, options) {
    const settings = Object.assign({ language: "vi" }, options || {});
    const language = settings.language === "en" ? "en" : "vi";
    const model = normalizeModel(input || {});
    const issues = [];
    const strengths = [];
    const items = allItems(model);
    const modules = model.modules || [];
    const details = model.details || [];
    const bounds = modelBounds(model);

    function text(vi, en) {
      return language === "vi" ? vi : en;
    }

    function addIssue(category, vi, en) {
      issues.push({ category, text: text(vi, en) });
    }

    function addStrength(category, vi, en) {
      strengths.push({ category, text: text(vi, en) });
    }

    if (modules.length >= 2) {
      addStrength("function", "Có nhiều zone chính, dễ đọc công năng.", "Multiple main zones make the function easy to read.");
    } else {
      addIssue("function", "Model chỉ có một module chính; nên tách rõ zone công năng.", "The model has only one main module; separate functional zones more clearly.");
    }

    if (details.length >= 6) {
      addStrength("craft", "Có đủ chi tiết để ảnh AI bám theo cấu tạo.", "There are enough details for image generation to follow the construction.");
    } else {
      addIssue("craft", "Chi tiết còn ít; nên bổ sung cánh, kệ, ray, tay nắm, khoảng trống kỹ thuật.", "Details are sparse; add doors, shelves, tracks, handles, and technical openings.");
    }

    const overflowItems = items.filter((item) => (
      item.x < 0 ||
      item.y < 0 ||
      item.z < 0 ||
      item.x + item.width > model.width ||
      item.y + item.height > model.height ||
      item.z + item.depth > model.depth
    ));
    if (overflowItems.length) {
      addIssue(
        "geometry",
        `Có ${overflowItems.length} chi tiết vượt khỏi kích thước tổng thể.`,
        `${overflowItems.length} items exceed the overall model dimensions.`
      );
    } else {
      addStrength("geometry", "Các chi tiết nằm trong kích thước tổng thể.", "Items stay inside the overall model dimensions.");
    }

    const hasVoid = details.some((item) => item.kind === "void");
    if (hasVoid) {
      addStrength("buildability", "Có khai báo khoảng trống/opening kỹ thuật.", "Technical voids/openings are declared.");
    }

    const hasMaterials = model.materials && Object.keys(model.materials).length > 0;
    if (hasMaterials) {
      addStrength("material", "Có vật liệu/màu nền để dẫn prompt ảnh AI.", "Material/color data is available for image prompting.");
    } else {
      addIssue("material", "Thiếu materials; prompt ảnh AI sẽ kém ổn định.", "Missing materials; AI image prompts will be less stable.");
    }

    const score = Math.max(0, Math.min(10, 10 - issues.length * 1.5));
    return {
      score,
      dimensions: {
        width: model.width,
        height: model.height,
        depth: model.depth,
        bounds
      },
      strengths,
      issues,
      checklist: buildIntakeChecklist({ language })
    };
  }

