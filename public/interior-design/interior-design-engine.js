  function render(options) {
    const mount = typeof options.mount === "string" ? document.querySelector(options.mount) : options.mount;
    if (!mount) throw new Error("InteriorDesigner.render: mount element not found.");

    const model = normalizeModel(options.model);
    const tabs = options.tabs || DEFAULT_TABS;
    const root = el("div", { class: "ide-root" });
    const shell = el("div", { class: "ide-shell" });
    root.appendChild(shell);

    shell.appendChild(el("header", { class: "ide-header" }, [
      el("div", {}, [
        el("h1", { class: "ide-title", text: model.title }),
        el("p", { class: "ide-subtitle", text: model.subtitle })
      ]),
      el("div", { class: "ide-badge" }, [
        el("div", { text: `Rộng: ${cm(model.width)}` }),
        el("div", { text: `Cao: ${cm(model.height)}` }),
        el("div", { text: `Sâu: ${cm(model.depth)}` })
      ])
    ]));

    const nav = el("nav", { class: "ide-tabs" });
    const views = el("main", {});
    shell.appendChild(nav);
    shell.appendChild(views);

    tabs.forEach((tab, index) => {
      const button = el("button", {
        class: `ide-tab${index === 0 ? " is-active" : ""}`,
        type: "button",
        text: TAB_LABELS[tab] || tab
      });
      const view = el("section", { class: `ide-view${index === 0 ? " is-active" : ""}` });

      button.addEventListener("click", () => {
        nav.querySelectorAll(".ide-tab").forEach((item) => item.classList.remove("is-active"));
        views.querySelectorAll(".ide-view").forEach((item) => item.classList.remove("is-active"));
        button.classList.add("is-active");
        view.classList.add("is-active");
        requestAnimationFrame(() => window.dispatchEvent(new Event("resize")));
      });

      view.appendChild(el("h2", { class: "ide-view-title", text: TAB_LABELS[tab] || tab }));
      if (tab === "front" || tab === "side" || tab === "plan") {
        view.appendChild(el("p", { class: "ide-note", text: "Render từ cùng model: modules là khối chính, details là cánh/kệ/ray/tay nắm/phụ kiện." }));
        view.appendChild(el("div", { class: "ide-canvas-wrap" }, [renderSvgView(model, tab)]));
      } else if (tab === "3d") {
        view.appendChild(el("p", { class: "ide-note", text: "Kéo để xoay, cuộn để zoom, double-click để reset." }));
        view.appendChild(el("div", { class: "ide-canvas-wrap" }, [render3d(model)]));
        view.appendChild(el("p", { class: "ide-hint", text: "3D dùng cùng module và detail với ba mặt chính, nên đổi kích thước sẽ đồng bộ." }));
      } else {
        view.appendChild(renderSpecs(model));
      }

      nav.appendChild(button);
      views.appendChild(view);
    });

    mount.innerHTML = "";
    mount.appendChild(root);
    return { model, root };
  }

